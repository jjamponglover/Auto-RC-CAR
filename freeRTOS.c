/* USER CODE BEGIN Header_StartTask02 */
/**
* @brief Function implementing the myTask02 thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTask02 */
void StartTask02(void *argument)
{
  /* USER CODE BEGIN StartTask02 */
  /* Infinite loop */
  for(;;)
  {
	  switch (rxData){
	  	case 'a':
	  		go();
	  		break;
	  	case 'b':
			back();
			break;
		case 'c':
			left();
			break;
		case 'd':
			right();
			break;
		case 'e':
			goright();
			break;
		case 'f':
			goleft();
			break;
		case 'g':
			backright();
			break;
		case 'h':
			backleft();
			break;
		case 's':
			stop();
			break;
		case 'i':
			autocar();
			break;
		default:
			stop();
			break;
	  }
    osDelay(5);
  }
  /* USER CODE END StartTask02 */
}

